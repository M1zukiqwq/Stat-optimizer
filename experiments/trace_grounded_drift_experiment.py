def build_case(trace: str, case_id: int, args: argparse.Namespace) -> TraceCase:
    seed = args.seed + case_id * 7919 + TRACE_ORDER.index(trace) * 1_000_003
    rng = random.Random(seed)
    np.random.seed(seed % (2**32 - 1))

    initial_rows = args.initial_rows + rng.randint(-args.initial_jitter, args.initial_jitter)
    data, null_count = init_trace_data(trace, rng, max(initial_rows, 500))
    table = ExtendedMemoryTable(data, null_count)
    prior_null = table.get_null_fraction()
    stale_boundaries = quantile_boundaries(table, args.num_buckets)
    base_time = datetime(2026, 5, 29, tzinfo=timezone.utc)

    observations: List[FeedbackObservation] = []
    events: List[TraceEvent] = []
    for step in range(args.observations):
        event = TRACE_APPLIERS[trace](table, rng, step, args.observations, args.event_scale)
        events.append(event)
        observations.append(
            draw_observation(
                rng,
                table,
                stale_boundaries=stale_boundaries,
                timestamp=base_time + timedelta(minutes=step),
            )
        )

    fresh_boundaries = quantile_boundaries(table, args.num_buckets)
    sample = KllFeedbackSample(
        prior=KllPrior(
            min_value=0.0,
            max_value=1.0,
            null_fraction=prior_null,
            quantile_levels=list(DEFAULT_QUANTILE_LEVELS),
            quantile_values=stale_boundaries[1:-1],
            value_type="double",
        ),
        observations=observations,
        corrected_quantile_values=fresh_boundaries[1:-1],
        source_path=f"trace:{trace}:{case_id}",
    )
    return TraceCase(
        sample=sample,
        trace_name=trace,
        case_id=case_id,
        initial_rows=initial_rows,
        final_rows=table.total_rows(),
        events=events,
        stale_boundaries=stale_boundaries,
        fresh_boundaries=fresh_boundaries,
    )


def trace_characteristics(cases: Sequence[TraceCase]) -> List[dict]:
    by_trace: Dict[str, List[TraceCase]] = defaultdict(list)
    for case in cases:
        by_trace[case.trace_name].append(case)

    result = []
    for trace in TRACE_ORDER:
        items = by_trace[trace]
        inserted = [sum(event.inserted for event in item.events) for item in items]
        updated = [sum(event.updated for event in item.events) for item in items]
        deleted = [sum(event.deleted for event in item.events) for item in items]
        total_ops = [i + u + d for i, u, d in zip(inserted, updated, deleted)]
        net_growth = [
            (item.final_rows - item.initial_rows) / max(item.initial_rows, 1)
            for item in items
        ]
        median_shift = [
            abs(item.fresh_boundaries[len(item.fresh_boundaries) // 2] - item.stale_boundaries[len(item.stale_boundaries) // 2])
            for item in items
        ]
        q_mae = [quantile_mae(item.stale_boundaries, item.fresh_boundaries) for item in items]

        def op_frac(values: Sequence[int], totals: Sequence[int]) -> float:
            return mean([value / max(total, 1) for value, total in zip(values, totals)])

        result.append({
            "trace": trace,
            "label": trace_label(trace),
            "benchmark_anchor": TRACE_TEMPLATES[trace].benchmark_anchor,
            "n_cases": len(items),
            "insert_frac": op_frac(inserted, total_ops),
            "update_frac": op_frac(updated, total_ops),
            "delete_frac": op_frac(deleted, total_ops),
            "net_growth_mean": mean(net_growth),
            "median_shift_mean": mean(median_shift),
            "stale_quantile_mae_mean": mean(q_mae),
            "ops_per_case_mean": mean(total_ops),
        })
    return result


def aggregate(rows: Sequence[TraceRow], hybrid_choices: Dict[str, Counter]) -> List[dict]:
    grouped: Dict[Tuple[str, str], List[TraceRow]] = defaultdict(list)
    for row in rows:
        grouped[(row.trace, row.method)].append(row)

    result = []
    for trace in TRACE_ORDER:
        stale_rows = grouped[(trace, "stale")]
        stale_qerr = geomean([row.qerror for row in stale_rows])
        for method in METHOD_ORDER:
            method_rows = grouped[(trace, method)]
            total_choices = sum(hybrid_choices[trace].values())
            result.append({
                "trace": trace,
                "label": trace_label(trace),
                "method": method,
                "n_cases": len(method_rows),
                "qerror_gm": geomean([row.qerror for row in method_rows]),
                "qerror_improvement_pct": (stale_qerr - geomean([row.qerror for row in method_rows])) / max(stale_qerr, 1e-12) * 100.0,
                "selectivity_mae_mean": mean([row.selectivity_mae for row in method_rows]),
                "quantile_mae_mean": mean([row.quantile_mae for row in method_rows]),
                "feedback_residual_mean": mean([row.feedback_residual for row in method_rows if math.isfinite(row.feedback_residual)]),
                "beats_stale_frac": mean([1.0 if row.beats_stale_qerr else 0.0 for row in method_rows]),
                "hybrid_isomer_frac": hybrid_choices[trace]["isomer"] / max(total_choices, 1),
                "hybrid_oasis_projected_frac": hybrid_choices[trace]["oasis_projected"] / max(total_choices, 1),
                "hybrid_oasis_frac": hybrid_choices[trace]["oasis"] / max(total_choices, 1),
                "hybrid_stale_frac": hybrid_choices[trace]["stale"] / max(total_choices, 1),
            })
    return result


def write_trace_table(output_dir: Path, summary: Sequence[dict], characteristics: Sequence[dict]) -> None:
    by_key = {(row["trace"], row["method"]): row for row in summary}
    chars = {row["trace"]: row for row in characteristics}
    with (output_dir / "table_trace_grounded_drift.tex").open("w") as handle:
        handle.write("\\begin{table*}[t]\n")
        handle.write("  \\centering\n")
        handle.write("  \\small\n")
        handle.write("  \\caption{Benchmark-inspired DML trace sanity check. Each trace emits explicit insert/update/delete events anchored to an analytical benchmark table/column pattern. Values are geometric mean selectivity Q-error; no production trace or query runtime is used.}\n")
        handle.write("  \\label{tab:trace_grounded_drift}\n")
        handle.write("  \\setlength{\\tabcolsep}{3.5pt}\n")
        handle.write("  \\resizebox{\\textwidth}{!}{%\n")
        handle.write("  \\begin{tabular}{lrrrrrrrr}\n")
        handle.write("    \\toprule\n")
        handle.write("    Trace & DML mix I/U/D & Growth & Stale & ISOMER & OASIS & OASIS-Proj & Hybrid & Fresh \\\\\n")
        handle.write("    \\midrule\n")
        for trace in TRACE_ORDER:
            char = chars[trace]
            dml = f"{char['insert_frac'] * 100:.0f}/{char['update_frac'] * 100:.0f}/{char['delete_frac'] * 100:.0f}"
            stale = by_key[(trace, "stale")]
            isomer = by_key[(trace, "isomer")]
            oasis = by_key[(trace, "oasis")]
            projected = by_key[(trace, "oasis_projected")]
            hybrid = by_key[(trace, "hybrid")]
            fresh = by_key[(trace, "fresh")]
            method_values = {
                "isomer": isomer["qerror_gm"],
                "oasis": oasis["qerror_gm"],
                "oasis_projected": projected["qerror_gm"],
                "hybrid": hybrid["qerror_gm"],
            }
            best = min(method_values, key=method_values.get)

            def fmt(method: str, value: float) -> str:
                text = f"{value:.3f}"
                return f"\\textbf{{{text}}}" if method == best else text

            handle.write(
                f"    {trace_label(trace)} & {dml} & {char['net_growth_mean'] * 100:+.1f}\\% & "
                f"{stale['qerror_gm']:.3f} & {fmt('isomer', isomer['qerror_gm'])} & "
                f"{fmt('oasis', oasis['qerror_gm'])} & {fmt('oasis_projected', projected['qerror_gm'])} & "
                f"{fmt('hybrid', hybrid['qerror_gm'])} & {fresh['qerror_gm']:.3f} \\\\\n"
            )
        handle.write("    \\bottomrule\n")
        handle.write("  \\end{tabular}%\n")
        handle.write("  }\n")
        handle.write("\\end{table*}\n")


def write_outputs(
    output_dir: Path,
    rows: Sequence[TraceRow],
    summary: Sequence[dict],
    characteristics: Sequence[dict],
    hybrid_choices: Dict[str, Counter],
    write_rows: bool,
) -> None:
    output_dir.mkdir(parents=True, exist_ok=True)
    with (output_dir / "summary.csv").open("w", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=list(summary[0].keys()))
        writer.writeheader()
        writer.writerows(summary)
    with (output_dir / "summary.json").open("w") as handle:
        json.dump(list(summary), handle, indent=2)
    with (output_dir / "trace_characteristics.csv").open("w", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=list(characteristics[0].keys()))
        writer.writeheader()
        writer.writerows(characteristics)
    with (output_dir / "trace_characteristics.json").open("w") as handle:
        json.dump(list(characteristics), handle, indent=2)
    with (output_dir / "hybrid_choices.json").open("w") as handle:
        json.dump({trace: dict(counter) for trace, counter in hybrid_choices.items()}, handle, indent=2)
    if write_rows:
        with (output_dir / "case_rows.csv").open("w", newline="") as handle:
            writer = csv.DictWriter(handle, fieldnames=list(asdict(rows[0]).keys()))
            writer.writeheader()
            for row in rows:
                writer.writerow(asdict(row))
    write_trace_table(output_dir, summary, characteristics)
    write_summary_text(output_dir, summary, characteristics, hybrid_choices)


def write_summary_text(
    output_dir: Path,
    summary: Sequence[dict],
    characteristics: Sequence[dict],
    hybrid_choices: Dict[str, Counter],
) -> None:
    by_key = {(row["trace"], row["method"]): row for row in summary}
    chars = {row["trace"]: row for row in characteristics}
    lines = [
        "Benchmark-inspired DML trace sanity check",
        "=" * 48,
        "OASIS checkpoint is trained on compound drift only; no trace retraining is used.",
        "",
        "Trace                  I/U/D    Growth  Stale  ISOMER  OASIS  Proj  Hybrid  Fresh",
        "-" * 90,
    ]
    for trace in TRACE_ORDER:
        char = chars[trace]
        dml = f"{char['insert_frac'] * 100:.0f}/{char['update_frac'] * 100:.0f}/{char['delete_frac'] * 100:.0f}"
        row = lambda method: by_key[(trace, method)]["qerror_gm"]
        lines.append(
            f"{trace:<22s} {dml:<8s} {char['net_growth_mean'] * 100:+6.1f}%  "
            f"{row('stale'):5.3f}  {row('isomer'):6.3f}  {row('oasis'):5.3f}  "
            f"{row('oasis_projected'):5.3f}  {row('hybrid'):6.3f}  {row('fresh'):5.3f}"
        )
        total = sum(hybrid_choices[trace].values())
        if total:
            lines.append(
                "  Hybrid choices: "
                + ", ".join(f"{method}={count / total * 100:.1f}%" for method, count in sorted(hybrid_choices[trace].items()))
            )
    text = "\n".join(lines)
    (output_dir / "summary.txt").write_text(text + "\n")
    print(text)


def run_experiment(args: argparse.Namespace) -> None:
    model = MlpHistogramModelV2.load(str(args.model_path))
    model_window = int(getattr(model, "max_observations", 16))
    traces = list(args.traces)

    cases: List[TraceCase] = []
    rows: List[TraceRow] = []
    hybrid_choices: Dict[str, Counter] = {trace: Counter() for trace in traces}
    total = len(traces) * args.cases_per_trace
    done = 0

    for trace in traces:
        for case_id in range(args.cases_per_trace):
            case = build_case(trace, case_id, args)
            cases.append(case)
            true_boundaries = case.fresh_boundaries
            boundaries, hybrid_choice = method_boundaries(case.sample, model, args.num_buckets, model_window)
            hybrid_choices[trace][hybrid_choice] += 1
            points = metric_points(args.seed + TRACE_ORDER.index(trace) * 100_000 + case_id, args.metric_points)
            observations = [
                {
                    "predicate_type": obs.predicate_type,
                    "value": obs.value,
                    "value_upper": obs.value_upper,
                    "actual_sel": obs.actual_selectivity,
                    "estimated_sel": obs.estimated_selectivity,
                }
                for obs in case.sample.observations
            ]
            stale_qerr = q_error(boundaries["stale"], true_boundaries, points)
            for method in METHOD_ORDER:
                method_qerr = q_error(boundaries[method], true_boundaries, points)
                rows.append(TraceRow(
                    trace=trace,
                    case_id=case_id,
                    method=method,
                    qerror=method_qerr,
                    selectivity_mae=selectivity_mae(boundaries[method], true_boundaries, points),
                    quantile_mae=quantile_mae(boundaries[method], true_boundaries),
                    feedback_residual=feedback_residual(boundaries[method], observations),
                    beats_stale_qerr=method_qerr < stale_qerr,
                ))
            done += 1
            if done % 50 == 0:
                print(f"Processed {done}/{total} trace cases")

    summary = aggregate(rows, hybrid_choices)
    characteristics = trace_characteristics(cases)
    write_outputs(args.output_dir, rows, summary, characteristics, hybrid_choices, write_rows=args.write_rows)


