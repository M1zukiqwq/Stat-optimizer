def weighted_choice_index(rng: random.Random, weights: Sequence[float]) -> int:
    total = sum(max(weight, 0.0) for weight in weights)
    if total <= 0:
        return rng.randrange(len(weights))
    target = rng.random() * total
    running = 0.0
    for idx, weight in enumerate(weights):
        running += max(weight, 0.0)
        if running >= target:
            return idx
    return len(weights) - 1


def weighted_sample_indices(
    table: ExtendedMemoryTable,
    count: int,
    weight_fn: Callable[[float], float],
) -> List[int]:
    if not table.data or count <= 0:
        return []
    count = min(count, len(table.data))
    weights = np.array([max(weight_fn(value), 1e-9) for value in table.data], dtype=float)
    total = float(weights.sum())
    if not math.isfinite(total) or total <= 0:
        return list(np.random.choice(len(table.data), size=count, replace=False))
    weights /= total
    return list(np.random.choice(len(table.data), size=count, replace=False, p=weights))


def delete_by_weight(table: ExtendedMemoryTable, rng: random.Random, count: int, weight_fn: Callable[[float], float]) -> int:
    if not table.data or count <= 0:
        return 0
    selected = weighted_sample_indices(table, count, weight_fn)
    for idx in sorted(selected, reverse=True):
        table.data.pop(idx)
    return len(selected)


def update_by_weight(
    table: ExtendedMemoryTable,
    rng: random.Random,
    count: int,
    weight_fn: Callable[[float], float],
    update_fn: Callable[[float], float],
) -> int:
    if not table.data or count <= 0:
        return 0
    selected = weighted_sample_indices(table, count, weight_fn)
    for idx in selected:
        table.data[idx] = clamp01(update_fn(table.data[idx]))
    return len(selected)


def insert_values(table: ExtendedMemoryTable, values: Sequence[float]) -> int:
    table.data.extend(clamp01(float(value)) for value in values)
    return len(values)


def clipped_normal(rng: random.Random, center: float, sigma: float, count: int) -> List[float]:
    return [clamp01(rng.normalvariate(center, sigma)) for _ in range(count)]


def init_trace_data(trace: str, rng: random.Random, size: int) -> Tuple[List[float], int]:
    if trace == "tpcds_sales_append":
        data = [np.random.beta(2.0, 2.8) for _ in range(size)]
    elif trace == "promotion_price_revision":
        data = [clamp01(np.random.lognormal(mean=-1.15, sigma=0.45)) for _ in range(size)]
    elif trace == "inventory_restock":
        data = clipped_normal(rng, 0.44, 0.15, size)
    elif trace == "returns_cancellation":
        data = [np.random.beta(2.4, 1.7) for _ in range(size)]
    elif trace == "customer_segment_churn":
        centers = [0.18, 0.45, 0.78]
        weights = [0.45, 0.35, 0.20]
        data = [clamp01(rng.normalvariate(rng.choices(centers, weights=weights)[0], 0.045)) for _ in range(size)]
    else:
        data = [clamp01(rng.normalvariate(0.5, 0.18)) for _ in range(size)]
    null_count = int(size * rng.uniform(0.01, 0.05))
    return [float(value) for value in data], null_count


def apply_sales_append(table: ExtendedMemoryTable, rng: random.Random, step: int, total_steps: int, scale: float) -> TraceEvent:
    center = 0.62 + 0.31 * (step / max(total_steps - 1, 1))
    inserted = insert_values(table, clipped_normal(rng, center, 0.045, int(rng.randint(70, 180) * scale)))
    deleted = 0
    if step % 5 == 4:
        deleted = delete_by_weight(table, rng, int(rng.randint(12, 38) * scale), lambda value: max(0.0, 0.32 - value))
    return TraceEvent(step=step, event_type="append_archive", inserted=inserted, deleted=deleted)


def apply_promotion_price(table: ExtendedMemoryTable, rng: random.Random, step: int, total_steps: int, scale: float) -> TraceEvent:
    inserted = int(rng.randint(25, 70) * scale)
    ins_count = insert_values(table, [clamp01(np.random.lognormal(mean=-1.05, sigma=0.38)) for _ in range(inserted)])
    if step % 4 in {1, 2}:
        updated = update_by_weight(
            table,
            rng,
            int(rng.randint(95, 190) * scale),
            lambda value: 0.2 + value,
            lambda value: value * rng.uniform(0.58, 0.82),
        )
        event = "promo_markdown"
    else:
        updated = update_by_weight(
            table,
            rng,
            int(rng.randint(45, 110) * scale),
            lambda value: max(0.0, 0.52 - value),
            lambda value: value + rng.uniform(0.04, 0.14),
        )
        event = "price_rebound"
    return TraceEvent(step=step, event_type=event, inserted=ins_count, updated=updated)


def apply_inventory_restock(table: ExtendedMemoryTable, rng: random.Random, step: int, total_steps: int, scale: float) -> TraceEvent:
    if step % 4 == 0:
        inserted = insert_values(table, clipped_normal(rng, 0.82, 0.055, int(rng.randint(90, 210) * scale)))
        updated = update_by_weight(
            table,
            rng,
            int(rng.randint(60, 130) * scale),
            lambda value: max(0.0, 0.42 - value),
            lambda value: value + rng.uniform(0.28, 0.45),
        )
        return TraceEvent(step=step, event_type="restock_batch", inserted=inserted, updated=updated)
    updated = update_by_weight(
        table,
        rng,
        int(rng.randint(100, 230) * scale),
        lambda value: 0.2 + value,
        lambda value: value - rng.uniform(0.04, 0.13),
    )
    deleted = delete_by_weight(table, rng, int(rng.randint(8, 24) * scale), lambda value: max(0.0, 0.18 - value))
    return TraceEvent(step=step, event_type="stock_depletion", updated=updated, deleted=deleted)


def apply_returns_cancellation(table: ExtendedMemoryTable, rng: random.Random, step: int, total_steps: int, scale: float) -> TraceEvent:
    inserted = insert_values(table, clipped_normal(rng, 0.16, 0.045, int(rng.randint(30, 80) * scale)))
    deleted = delete_by_weight(table, rng, int(rng.randint(35, 95) * scale), lambda value: value ** 2.5)
    updated = 0
    if step % 3 == 2:
        updated = update_by_weight(
            table,
            rng,
            int(rng.randint(45, 110) * scale),
            lambda value: value,
            lambda value: value * rng.uniform(0.65, 0.90),
        )
    return TraceEvent(step=step, event_type="return_cancel", inserted=inserted, updated=updated, deleted=deleted)


def apply_customer_churn(table: ExtendedMemoryTable, rng: random.Random, step: int, total_steps: int, scale: float) -> TraceEvent:
    centers = [0.16, 0.48, 0.82]
    target = centers[(step // 3 + 1) % len(centers)]
    inserted = insert_values(table, clipped_normal(rng, target, 0.035, int(rng.randint(35, 95) * scale)))
    updated = update_by_weight(
        table,
        rng,
        int(rng.randint(70, 155) * scale),
        lambda value: 0.15 + abs(value - 0.48),
        lambda value: 0.72 * value + 0.28 * target + rng.uniform(-0.025, 0.025),
    )
    deleted = 0
    if step % 5 == 3:
        deleted = delete_by_weight(table, rng, int(rng.randint(10, 35) * scale), lambda value: max(0.0, 0.22 - abs(value - 0.48)))
    return TraceEvent(step=step, event_type="segment_migration", inserted=inserted, updated=updated, deleted=deleted)


def apply_seasonal_mixed(table: ExtendedMemoryTable, rng: random.Random, step: int, total_steps: int, scale: float) -> TraceEvent:
    phase = math.sin(2.0 * math.pi * step / 6.0)
    center = 0.5 + 0.24 * phase
    inserted = insert_values(table, clipped_normal(rng, center, 0.07, int(rng.randint(45, 115) * scale)))
    updated = update_by_weight(
        table,
        rng,
        int(rng.randint(25, 80) * scale),
        lambda value: 0.1 + abs(value - (0.5 - 0.24 * phase)),
        lambda value: 0.88 * value + 0.12 * center + rng.uniform(-0.025, 0.025),
    )
    deleted = 0
    if step % 4 == 1:
        deleted = delete_by_weight(table, rng, int(rng.randint(10, 32) * scale), lambda value: abs(value - (0.5 - 0.24 * phase)))
    return TraceEvent(step=step, event_type="seasonal_mixed", inserted=inserted, updated=updated, deleted=deleted)


